def bits_to_byte(bits: bitarray) -> int:
    return int(bits.to01(), 2)


def int_to_bits(num: int) -> bitarray:
    byte1 = (num >> 24) & 0xFF
    byte2 = (num >> 16) & 0xFF
    byte3 = (num >> 8) & 0xFF
    byte4 = num & 0xFF
    bitarray_bytes = bytes([byte1, byte2, byte3, byte4])
    bits = [f'{int(str(i), 10):08b}' for i in bitarray_bytes]
    bits = ''.join(bits)
    return bitarray(bits)


def bitwise_add(a: bitarray, b: bitarray) -> bitarray:
    int_a = int(a.to01(), 2)
    int_b = int(b.to01(), 2)
    result = (int_a + int_b) % (2 ** 32)
    return bitarray(bin(result)[2:].zfill(32))


def get_data_binary(filename: str) -> bytes:
    with open(filename, 'rb') as file:
        arr = file.read()
    return arr


def data_to_block(data_block: bytes) -> list:
    block = bitarray()
    block.frombytes(data_block)
    left = block >> 32
    right = block & ffffffff
    left = left[32:]
    right = right[32:]
    return [left, right]


def func(x: bitarray) -> bitarray:
    res1 = s[0][int((x >> 24 & ff).to01(), 2)]
    res2 = res1 ^ s[1][int((x >> 16 & ff).to01(), 2)]
    res3 = res2 ^ s[2][int((x >> 8 & ff).to01(), 2)]
    res4 = res3 ^ s[3][int((x & ff).to01(), 2)]
    return res4


def blowfish_encrypt(left: bitarray, right: bitarray) -> list:
    for i in range(16):
        left = left ^ p[i]
        right = func(left) ^ right
        left, right = right, left
    left, right = right, left
    right = right ^ p[16]
    left = left ^ p[17]
    return [left, right]


def blowfish_decrypt(left: bitarray, right: bitarray) -> list:
    for i in range(17, 1, -1):
        left = left ^ p[i]
        right = func(left) ^ right
        left, right = right, left
    left, right = right, left
    right = right ^ p[1]
    left = left ^ p[0]
    return [left, right]


def init_key() -> None:
    q = 0
    for i in range(18):
        k = bitarray(32)
        for j in range(4):
            k = (k << 8) | key[q]
            q = (q + 1) % len(key)
        p[i] ^= k


